package tw.org.iii.myjava;

public class Brad03 {

	public static void main(String[] args) {
		int a = 10, b = 3;
		int c1 = a + b; 
		int c2 = a - b; 
		int c3 = a * b; 
		int c4 = a / b; 
		int c5 = a % b;
		System.out.printf("%d + %d = %d\n", a, b, c1);
		System.out.printf("%d - %d = %d\n", a, b, c2);
		System.out.printf("%d x %d = %d\n", a, b, c3);
		System.out.printf("%d / %d = %d\n", a, b, c4);
		System.out.printf("%d %% %d = %d\n", a, b, c5);
	}

}
